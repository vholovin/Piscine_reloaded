int		ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int nbr;

	i = 0;
	nbr = 0;
	while (tab[i])
	{
		if (f(tab[i]))
			nbr++;
		i++;
	}
	return (nbr);
}
