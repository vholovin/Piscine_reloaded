int		ft_sqrt(int nb)
{
	int buf;
	int i;

	i = 1;
	buf = 0;
	while (i <= nb / 2)
	{
		buf = i * i;
		if (buf == nb)
			return (i);
		i++;
	}
	return (0);
}
